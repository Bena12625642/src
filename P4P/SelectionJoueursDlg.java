    private void BajouteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BajouteActionPerformed
        // TODO add your handling code here:
        int index=LJoueursDispo.getSelectedIndex();
        if(index!=-1){
            String j=LJoueursDispo.getSelectedValue();
            modeljdispo.remove(index);
            modelj.addElement(j);//met dans le model le pseudo selectionner
            Ljoueurs.setModel(modelj);//met dans la jliste le model qui contient le pseudo
        }
    }//GEN-LAST:event_BajouteActionPerformed

